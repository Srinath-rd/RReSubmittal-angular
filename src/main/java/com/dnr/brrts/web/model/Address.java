package com.dnr.brrts.web.model;

public class Address {

    private String streetAddress1;
    private String streetAddress2;

    private String city;
    private String county;
    private String municipality;
    private Double latitude;
    private Double longitude;
    private String region;
    private String zipcode;
    private String state;

    @Override
    public String toString() {
        return "Address [city=" + city + ", county=" + county + ", latitude=" + latitude + ", longitude=" + longitude
                + ", municipality=" + municipality + ", streetAddress1=" + streetAddress1 + ", streetAddress2="
                + streetAddress2 + ", zipcode=" + zipcode + "]";
    }

    public String getStreetAddress1() {
        return streetAddress1;
    }

    public void setStreetAddress1(String streetAddress1) {
        this.streetAddress1 = streetAddress1;
    }

    public String getStreetAddress2() {
        return streetAddress2;
    }

    public void setStreetAddress2(String streetAddress2) {
        this.streetAddress2 = streetAddress2;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getCounty() {
        return county;
    }

    public void setCounty(String county) {
        this.county = county;
    }

    public String getMunicipality() {
        return municipality;
    }

    public void setMunicipality(String municipality) {
        this.municipality = municipality;
    }

    public Double getLatitude() {
        return latitude;
    }

    public void setLatitude(Double latitude) {
        this.latitude = latitude;
    }

    public Double getLongitude() {
        return longitude;
    }

    public void setLongitude(Double longitude) {
        this.longitude = longitude;
    }

    public String getZipcode() {
        return zipcode;
    }

    public void setZipcode(String zipcode) {
        this.zipcode = zipcode;
    }

    public String getRegion() {
        return region;
    }

    public void setRegion(String region) {
        this.region = region;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

}
